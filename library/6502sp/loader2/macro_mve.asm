\       Name: MVE
\ ******************************************************************************

MACRO MVE S%, D%, PA%

  LDA #(S%MOD256)
  STA Z1
  LDA #(S%DIV256)
  STA Z1+1
  LDA #(D%MOD256)
  STA Z2
  LDA #(D%DIV256)
  STA Z2+1
  LDX #PA%
  JSR MVBL

ENDMACRO


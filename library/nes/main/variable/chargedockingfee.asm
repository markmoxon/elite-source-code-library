.chargeDockingFee

 SKIP 1                 \ Records whether we have been charged a docking fee, so
                        \ we don't get charged twice:
                        \
                        \   * 0 = we have not been charged a docking fee
                        \
                        \   * Non-zero = we have been charged a docking fee
                        \
                        \ The docking fee is 5.0 credits


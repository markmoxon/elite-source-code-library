.autoPlayKey

 SKIP 1                 \ Stores the buttons to be automatically pressed during
                        \ auto-play
                        \
                        \ The bits are as follows:
                        \
                        \   * Bit 0 = right button
                        \   * Bit 1 = left button
                        \   * Bit 2 = down button
                        \   * Bit 3 = up button
                        \   * Bit 4 = Select button
                        \   * Bit 5 = B button
                        \   * Bit 6 = A button
                        \
                        \ Bit 7 is always clear

